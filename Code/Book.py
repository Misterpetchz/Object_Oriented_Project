class Book : 
    def __init__(self, product_id, cover, brief, creator, name, book_info, book_ISBN_id, book_publisher, book_preview, 
                 critic_review, table_of_content, summary, genre, date_created, rating, discount, catalog):
        self.product_id = product_id
        self.cover = cover
        self.brief = brief
        self.creator = creator
        self.name = name
        self.book_info = book_info
        self.book_ISBN_id = book_ISBN_id
        self.book_publisher = book_publisher
        self.book_preview = book_preview
        self.critic_review = critic_review
        self.table_of_content = table_of_content
        self.summary = summary
        self.genre = genre
        self.date_created = date_created
        self.rating = rating
        self.discount = discount
        self.catalog = catalog
